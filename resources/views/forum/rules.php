<h2>Forum Rules</h2>
<ol>
    <li>Be courteous and civil at all times. Angry, coarse, insults or any malicious language is not permitted.</li>
    <li>Do not post or link to illegal, offensive, or sexually oriented images or content.</li>
    <li>Thread discussion topics must be related to the Workman Layout, other keyboard layouts, and ergonomics.</li>
    <li>Keep your posts/responses on topic. Do not hijack threads.</li>
    <li>No useless posts. This includes: Thread bumping, useless one liners, flamewars, neffing, trolling.</li>
    <li>No spamming or advertising.</li>
    <li>All posts must be in English.</li>
    <li>Do not post in ALL CAPS.</li>
    <li>Do not pretend to be someone else or create multiple/throwable accounts for the purpose of pretending to be
        someone else.
    </li>
</ol>

Failure to abide by these rules may result in an editing, negative moderation or deletion of your post.
